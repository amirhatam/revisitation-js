console.log("test inférieur ou égal bis :", test <= bis);
console.log("test égal à bis :", test == bis);
console.log("test strictement égal à bis :", test === bis);
console.log("test différent de bis :", test != bis);
console.log("test strictement différent de bis :", test !== bis);